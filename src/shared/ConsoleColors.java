package shared;

public class ConsoleColors {
    // Reset
    public static final String RESET = "\033[0m";  // Text Reset

    // Regular Colors
    public static final String BLACK = "\033[0;30m";   // BLACK
    public static final String RED = "\033[0;31m";     // RED
    public static final String GREEN = "\033[0;32m";   // GREEN
    public static final String YELLOW = "\033[0;33m";  // YELLOW
    public static final String BLUE = "\033[0;34m";    // BLUE
    public static final String PURPLE = "\033[0;35m";  // PURPLE
    public static final String CYAN = "\033[0;36m";    // CYAN
    public static final String WHITE = "\033[0;37m";   // WHITE

    // Bold
    public static final String BLACK_BOLD = "\033[1;30m";  // BLACK
    public static final String RED_BOLD = "\033[1;31m";    // RED
    public static final String GREEN_BOLD = "\033[1;32m";  // GREEN
    public static final String YELLOW_BOLD = "\033[1;33m"; // YELLOW
    public static final String BLUE_BOLD = "\033[1;34m";   // BLUE
    public static final String PURPLE_BOLD = "\033[1;35m"; // PURPLE
    public static final String CYAN_BOLD = "\033[1;36m";   // CYAN
    public static final String WHITE_BOLD = "\033[1;37m";  // WHITE

    // Underline
    public static final String BLACK_UNDERLINED = "\033[4;30m";  // BLACK
    public static final String RED_UNDERLINED = "\033[4;31m";    // RED
    public static final String GREEN_UNDERLINED = "\033[4;32m";  // GREEN
    public static final String YELLOW_UNDERLINED = "\033[4;33m"; // YELLOW
    public static final String BLUE_UNDERLINED = "\033[4;34m";   // BLUE
    public static final String PURPLE_UNDERLINED = "\033[4;35m"; // PURPLE
    public static final String CYAN_UNDERLINED = "\033[4;36m";   // CYAN
    public static final String WHITE_UNDERLINED = "\033[4;37m";  // WHITE

    // Bold and Underline
    public static final String BLACK_BOLD_UNDERLINED = "\033[1;4;30m";  // BLACK
    public static final String RED_BOLD_UNDERLINED = "\033[1;4;31m";    // RED
    public static final String GREEN_BOLD_UNDERLINED = "\033[1;4;32m";  // GREEN
    public static final String YELLOW_BOLD_UNDERLINED = "\033[1;4;33m"; // YELLOW
    public static final String BLUE_BOLD_UNDERLINED = "\033[1;4;34m";   // BLUE
    public static final String PURPLE_BOLD_UNDERLINED = "\033[1;4;35m"; // PURPLE
    public static final String CYAN_BOLD_UNDERLINED = "\033[1;4;36m";   // CYAN
    public static final String WHITE_BOLD_UNDERLINED = "\033[1;4;37m";  // WHITE

    // Background
    public static final String BLACK_BACKGROUND = "\u001B[40m";  // BLACK
    public static final String RED_BACKGROUND = "\u001B[41m";    // RED
    public static final String GREEN_BACKGROUND = "\u001B[42m";  // GREEN
    public static final String YELLOW_BACKGROUND = "\u001B[43m"; // YELLOW
    public static final String BLUE_BACKGROUND = "\u001B[44m";   // BLUE
    public static final String PURPLE_BACKGROUND = "\u001B[45m"; // PURPLE
    public static final String CYAN_BACKGROUND = "\u001B[46m";   // CYAN
    public static final String WHITE_BACKGROUND = "\u001B[47m";  // WHITE

    // Bold and Background
    public static final String BLACK_BOLD_BACKGROUND = "\u001B[1;40m";  // BLACK
    public static final String RED_BOLD_BACKGROUND = "\u001B[1;41m";    // RED
    public static final String GREEN_BOLD_BACKGROUND = "\u001B[1;42m";  // GREEN
    public static final String YELLOW_BOLD_BACKGROUND = "\u001B[1;43m"; // YELLOW
    public static final String BLUE_BOLD_BACKGROUND = "\u001B[1;44m";   // BLUE
    public static final String PURPLE_BOLD_BACKGROUND = "\u001B[1;45m"; // PURPLE
    public static final String CYAN_BOLD_BACKGROUND = "\u001B[1;46m";   // CYAN
    public static final String WHITE_BOLD_BACKGROUND = "\u001B[1;47m";  // WHITE
}

